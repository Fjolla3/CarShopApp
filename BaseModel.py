class Car:
    price= 0.0
    name = str
    speed= 0.0
    

    def getName(self):
        return self.name
    
    def setName(self, name):
        self.name = name

    def getSpeed(self):
        return self.speed
    
    def setSpeed(self, speed):
        self.speed = speed

    def getPrice(self):
        return self.price
    
    def setPrice(self, price):
        self.price = price




